package day09_IfStatements;

public class MinimumNumber {

    public static void main(String[] args) {

        int number1 = 174,
                number2 = 196;

        if (number1 < number2){
            System.out.println(number1 + " is the minimum number.");
        }else
            System.out.println(number2 + " is the minimum number.");
    }
}
